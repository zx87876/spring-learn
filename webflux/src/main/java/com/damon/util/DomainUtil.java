    private static HashMap<String, Boolean> rootMap = new HashMap();

    static {

        for (String s : ianaRootZone) {
            rootMap.put(s, Boolean.TRUE);
        }

        for (String s : leave2RootZone) {
            //非英文根域转为 punycode
            if (!ValidationUtil.isEnglishString(s)) {
                s = IDN.toASCII(s);
            }
            rootMap.put(s, Boolean.TRUE);
        }
    }


    private static HashMap<String, HashMap<String, Boolean>> twoMap = new HashMap();

    static {

        for (String s : ianaRootZone) {
            twoMap.put(s, new HashMap<>());
        }

        for (String s : leave2RootZone) {
            //非英文根域转为 punycode
            if (!ValidationUtil.isEnglishString(s)) {
                s = IDN.toASCII(s);
            }
            String[] sl = s.split("\\.");
            if (sl.length >= 2) {
                HashMap<String, Boolean> m = twoMap.get(sl[1]);

                if (m != null) {
                    m.put(sl[0], Boolean.TRUE);
                }
            }
        }
    }


    public static String getEEFileAuthDomain(String str) {
        if (ValidationUtil.isWildcard(str)) {
            return str.replace("*.", "");
        }
        if (str.startsWith("www.")) {
            String domain = str.substring(str.indexOf(".") + 1);
            String mainDomain = com.trustasia.trustcenter.base.util.DomainUtil.splitMasterDomain(str);
            if (mainDomain.equalsIgnoreCase(domain)) {
                return mainDomain;
            } else {
                return str;
            }
        } else {
            return str;
        }

    }

